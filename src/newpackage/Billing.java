    public void update()
    {
            NewQty = OldQty - Integer.parseInt(QUANTITY.getText());
            
        try{
            Con = DriverManager.getConnection("jdbc:derby://localhost:1527/Java_Project","User1","user1");
            String UpdateQuery = "UPDATE User1.MEDICINE SET QUANTITY = "+NewQty+""+"Where ID + "+Medid;
            Statement Add = Con.createStatement();
            Add.executeUpdate(UpdateQuery);
            JOptionPane.showMessageDialog(this, "The Medicine is Updated.");
                
           }catch(SQLException e)
            {
                e.printStackTrace();
            }
            SelectMed();
            
    }
    
