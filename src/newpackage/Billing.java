    private void AddBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddBtnMouseClicked

        BillTxt.setText(BillTxt.getText()+"\t\t******************** Maruti Medicals ******************** \n"
            + "\tID   \tMEDICINE   \tQUANTITY   \tPRICE (per unit)   \n");
        if(Med.getText().isEmpty() || QUANTITY.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Missing Information.");
        }
        else{
            i++;
        update();
        
        if(i==1)
        {
            BillTxt.setText(BillTxt.getText()+"\t\t******************** Maruti Medicals ******************** \n"
            + "\tID   \tMEDICINE   \tQUANTITY   \tPRICE (per unit)   \n"
            + "\t\t"+i+"    \t"+Med.getText()+"    \t"+QUANTITY.getText()+"    \t"+price+"\n");
        }
        else{
            BillTxt.setText(BillTxt.getText()+i+"   \t"+Med.getText()+"    \t"+QUANTITY.getText()+"    \t"+price+"\n");
        }
      }
          
    }//GEN-LAST:event_AddBtnMouseClicked

